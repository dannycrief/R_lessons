# Creating vectors
people <- c("Sara", "Alan", "Zenon")
print(people)
people_length <- length(people)
print(people_length)

numbers <- c(1, 2, 3, 4, 5)
print(numbers)
numbers_length <- length(numbers)
print(numbers_length)

one_to_seventy <- seq(1, 70)
print(one_to_seventy)
odds <- seq(1, 10, 2)
print(odds)

one_to_seventy <- 1:70
print(one_to_seventy)

